export function detectLang() {
  const lang = (navigator.language || navigator.userLanguage || 'en').split('-')[0].toLowerCase();
  const supported = ['tr', 'en', 'de', 'fr', 'es', 'ar', 'ru', 'da'];
  return supported.includes(lang) ? lang : 'en';
}

